// Q7: Write a program to calculate root of any number.
// i.e: √y = y1⁄2

import 'dart:math';

void main() {
  // Number to calculate the square root
  double number = 25;

  // Calculate the square root
  double squareRoot = sqrt(number);

  // Print the result
  print("Square root of $number is: $squareRoot");
}
